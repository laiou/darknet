//参数中的nms就是nms的阈值。。从yolo层调用这个函数。。这里的total就是
void do_nms_sort(detection *dets, int total, int classes, float thresh)
{
    int i, j, k;
    k = total-1;
    //这里total其实就是框的总数
    for(i = 0; i <= k; ++i){
        //根据框的预测置信度筛选。。置信度等于0的删除
        if(dets[i].objectness == 0){
            detection swap = dets[i];
            dets[i] = dets[k];
            dets[k] = swap;
            --k;
            --i;
        }
    }
    //更新total的数值
    total = k+1;

    for(k = 0; k < classes; ++k){
        //更新dets中的sort_class变量。。标记当前是按照哪一个类别进行的排序
        for(i = 0; i < total; ++i){
            dets[i].sort_class = k;
        }
        //qsort排序函数。。void qsort(void *base, size_t nitems, size_t size, int (*compar)(const void *a, const void*b))
        //base -- 指向要排序的数组的第一个元素的指针。
        //nitems -- 由 base 指向的数组中元素的个数。
        //size -- 数组中每个元素的大小，以字节为单位。
        //compar -- 用来比较两个元素的函数。
        //关于nms_comparator的细节参考src/box.c
        //然后就是关于qsort的排序。。参数中的compar的返回值如果小于0，则a排在b前面。。。如果等于0，则a和b具体位置不确定，如果大于0,则a排在b的后面
        //这里qsort之后是根据指定的类别的框的prob的降序
        qsort(dets, total, sizeof(detection), nms_comparator);
        //遍历全部框。。。利用iou删除重复的
        for(i = 0; i < total; ++i){
            if(dets[i].prob[k] == 0) continue;
            box a = dets[i].bbox;
            for(j = i+1; j < total; ++j){
                box b = dets[j].bbox;
                //计算两个框的iou。。具体细节参考src/box.c
                if (box_iou(a, b) > thresh){
                    dets[j].prob[k] = 0;
                }
            }
        }
    }
}

box float_to_box(float *f, int stride)
{
    box b = {0};
    b.x = f[0];
    b.y = f[1*stride];
    b.w = f[2*stride];
    b.h = f[3*stride];
    return b;
}

dbox derivative(box a, box b)
{
    dbox d;
    d.dx = 0;
    d.dw = 0;
    float l1 = a.x - a.w/2;
    float l2 = b.x - b.w/2;
    if (l1 > l2){
        d.dx -= 1;
        d.dw += .5;
    }
    float r1 = a.x + a.w/2;
    float r2 = b.x + b.w/2;
    if(r1 < r2){
        d.dx += 1;
        d.dw += .5;
    }
    if (l1 > r2) {
        d.dx = -1;
        d.dw = 0;
    }
    if (r1 < l2){
        d.dx = 1;
        d.dw = 0;
    }

    d.dy = 0;
    d.dh = 0;
    float t1 = a.y - a.h/2;
    float t2 = b.y - b.h/2;
    if (t1 > t2){
        d.dy -= 1;
        d.dh += .5;
    }
    float b1 = a.y + a.h/2;
    float b2 = b.y + b.h/2;
    if(b1 < b2){
        d.dy += 1;
        d.dh += .5;
    }
    if (t1 > b2) {
        d.dy = -1;
        d.dh = 0;
    }
    if (b1 < t2){
        d.dy = 1;
        d.dh = 0;
    }
    return d;
}
//计算两个框相交的部分的边长。。画个图就能看出来了。。
float overlap(float x1, float w1, float x2, float w2)
{
    float l1 = x1 - w1/2;
    float l2 = x2 - w2/2;
    float left = l1 > l2 ? l1 : l2;
    float r1 = x1 + w1/2;
    float r2 = x2 + w2/2;
    float right = r1 < r2 ? r1 : r2;
    return right - left;
}
//计算两个框的交集
float box_intersection(box a, box b)
{
    //overlap参考src/box.c。。
    float w = overlap(a.x, a.w, b.x, b.w);
    float h = overlap(a.y, a.h, b.y, b.h);
    if(w < 0 || h < 0) return 0;
    float area = w*h;
    return area;
}
//计算两个框的并集。。。
float box_union(box a, box b)
{
    float i = box_intersection(a, b);
    float u = a.w*a.h + b.w*b.h - i;
    return u;
}
//计算两个框的box
float box_iou(box a, box b)
{//box_intersection计算两个框的交集具体参考src/box.c
    //box_union计算两个框的并集，具体参考src/box.c
    return box_intersection(a, b)/box_union(a, b);
}

float box_rmse(box a, box b)
{
    return sqrt(pow(a.x-b.x, 2) + 
                pow(a.y-b.y, 2) + 
                pow(a.w-b.w, 2) + 
                pow(a.h-b.h, 2));
}

dbox dintersect(box a, box b)
{
    float w = overlap(a.x, a.w, b.x, b.w);
    float h = overlap(a.y, a.h, b.y, b.h);
    dbox dover = derivative(a, b);
    dbox di;

    di.dw = dover.dw*h;
    di.dx = dover.dx*h;
    di.dh = dover.dh*w;
    di.dy = dover.dy*w;

    return di;
}

dbox dunion(box a, box b)
{
    dbox du;

    dbox di = dintersect(a, b);
    du.dw = a.h - di.dw;
    du.dh = a.w - di.dh;
    du.dx = -di.dx;
    du.dy = -di.dy;

    return du;
}


